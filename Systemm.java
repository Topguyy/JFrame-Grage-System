    private void AddCarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddCarActionPerformed
        String Name = ClientsName.getText();
        String ID = ClientsID.getText();
        String PhoneNumber = ClientsPhoneNumber.getText();
        String CarPlate = CarsPlates.getText();
        String CarModel = CarsModels.getText();
        if (Name.isEmpty() || ID.isEmpty() || PhoneNumber.isEmpty() || CarPlate.isEmpty() || CarModel.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all the clients informations and try again");
        } else {
            DefaultTableModel model = (DefaultTableModel) ClientsTable.getModel();
            model.addRow(new Object[]{Name, ID, PhoneNumber, CarPlate, CarModel});
            ClientsName.setText("");
            ClientsID.setText("");
            ClientsPhoneNumber.setText("");
            CarsPlates.setText("");
            CarsModels.setText("");
        }
    }//GEN-LAST:event_AddCarActionPerformed

