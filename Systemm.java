    private void DeleteCarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteCarActionPerformed
        int row = ClientsTable.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "No row is selected,Please select one!!!");
        } else {
            DefaultTableModel model = (DefaultTableModel) ClientsTable.getModel();
            model.removeRow(row);
        }
    }//GEN-LAST:event_DeleteCarActionPerformed

    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        DefaultTableModel model = (DefaultTableModel) ClientsTable.getModel();
        Vector<Vector> tableData = model.getDataVector();
        try {
            FileOutputStream file = new FileOutputStream("file.bin");
            ObjectOutputStream output = new ObjectOutputStream(file);
            output.writeObject(tableData);
            output.close();
            file.close();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_formWindowClosing

